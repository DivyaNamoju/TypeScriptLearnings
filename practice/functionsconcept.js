// names functions
function sum(x, y) {
    return x + y;
}
// anonymous function
var sumofTwoNumbers = function (x, y) {
    return x + y;
};
//Arrow functions
var calculateSum = function (x, y) { return x + y; };
console.log(sum(345, 654));
console.log(sumofTwoNumbers(87900000, 78));
console.log(calculateSum(678.00908, 675));
